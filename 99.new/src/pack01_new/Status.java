package pack01_new;

public class Status {
	int power, agi, inte, nar, app, hp, fame;
	
	public Status() {
		this.power = 0;
		this.agi = 0;
		this.inte = 0;
		this.nar = 0;
		this.app = 0;
		this.hp = 0;
		this.fame = 0;
	}

	public Status(int power, int agi, int inte, int nar, int app, int hp, int fame) {
		this.power = power;
		this.agi = agi;
		this.inte = inte;
		this.nar = nar;
		this.app = app;
		this.hp = hp;
		this.fame = fame;
	}
}
