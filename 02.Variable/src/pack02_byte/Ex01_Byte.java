package pack02_byte;

public class Ex01_Byte {
	public static void main(String[] args) {
		byte var1;
		var1 = -128;
		byte var2;
		var2 = 127;
		System.out.println(var1);
		System.out.println(var2);
	}
}

//변수타입 변수명 ; <= 선언
//선언된 변수 = 값 ; <= 할당
//byte는 관련된 8개의 비트들로 구성되어있음.
//bit 0또는 1 두가지 정보를 표현할 수 있는
//최소한의 정보 표현 단위이다.
//-128 ~ 127 표현 가능
//※변수는 표현 범위를 가지기 때문에 해당하는 범위 안에서 사용※
