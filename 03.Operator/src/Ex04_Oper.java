
public class Ex04_Oper {
	public static void main(String[] args) {
		//사칙연산 + 나머지
		int value1 = 5, value2 = 2;
		//두 변수의 사칙연산 결과를 출력해보기. ex)출력결과:value1 + value2 = 7..
		System.out.println("value1 + value2 = "+ (value1 + value2));
		System.out.println("value1 - value2 = "+ (value1 - value2));
		System.out.println("value1 * value2 = "+ (value1 * value2));
		System.out.println("value1 / value2 = "+ (value1 / value2));
		System.out.println("value1 % value2 (나머지)= "+ (value1 % value2));
		//습관적으로 연산식과 String이 더해질때는 딸 감싸주기
		
		
	}
}
